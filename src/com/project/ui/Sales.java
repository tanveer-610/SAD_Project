    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         if(jTable1.getSelectedRow()==-1)
        {
            JOptionPane.showMessageDialog(this,"Select a Row");
            
        }else
         {
             int index=jTable1.getSelectedRow();
             String pid= jTable1.getValueAt(index, 0).toString();
             String qnty= jTable1.getValueAt(index, 2).toString();
             String stotal= jTable1.getValueAt(index, 4).toString();
             
             Product.disSelected(Integer.parseInt(pid),Integer.parseInt(qnty));
             double bill=Double.parseDouble(tbill.getText());
             bill=bill-(Double.parseDouble(stotal));
             tbill.setText(String.valueOf(bill));
             model.removeRow(jTable1.getSelectedRow());
         }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(!txtPID.getText().isEmpty() && !txtQnt.getText().isEmpty())
        {
            Product.addForSell(txtPID.getText(), txtQnt.getText());
            txtPID.setText("");
            txtQnt.setText("");
        }else if(txtPID.getText().isEmpty() || txtQnt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Give product id & quantity.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

