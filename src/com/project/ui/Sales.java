     public static void sellDataAdd(String pid,String name,String qnt,String uPrice,String stotal)
     {
         String data[]={pid,name,qnt,uPrice,stotal};
         model.addRow(data);
         if(tbill.getText().isEmpty())
         {
             tbill.setText(stotal);
         }else
         {
             double total=Double.parseDouble(tbill.getText());
             double st=Double.parseDouble(stotal);
             double totalBill=total+st;
             tbill.setText(String.valueOf(totalBill));
         }
         
     }
     
